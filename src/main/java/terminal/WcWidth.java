    /**
     * Optimized binary search on a flat array of ranges.
     */
    private static boolean inTable(int[] table, int codePoint) {
        int bot = 0;
        int top = (table.length / 2) - 1;

        while (top >= bot) {
            int mid = (bot + top) / 2;
            int first = table[mid * 2];
            int last = table[mid * 2 + 1];

            if (last < codePoint) {
                bot = mid + 1;
            } else if (first > codePoint) {
                top = mid - 1;
            } else {
                return true;
            }
        }
        return false;
    }

    /**
     * Main API method. Returns the logical width of a Unicode character in the terminal buffer.
     * @param codePoint The character as a full Unicode Code Point (int).
     * @return:
     * 1 - Standard character (A-Z, digits, etc.)
     * 2 - Wide character (Emoji, East Asian CJK characters)
     * 0 - Zero-width character (non-printing or a modifier that combines with the previous character)
     */
    public static int calculateWidth(int codePoint) {
        if (codePoint < 0 || codePoint > 0x10ffff) {
            return 0; // an invalid character is treated as if it had a width of 0
        }

        if (inTable(NONPRINT, codePoint)) {
            return 0;
        }

        if (inTable(COMBINING, codePoint)) {
            return 0;
        }

        if (inTable(DOUBLE_WIDTH, codePoint)) {
            return 2;
        }

        if (inTable(EMOJI_WIDTH, codePoint)) {
            return 2;
        }

        // If a character is not in any of the lists, it defaults to a single cell.
        // (Note: wcwidth9 also has the ambiguous, unassigned, and private_use arrays.
        // The standard way is to treat them as a single cell characters.)
        return 1;
    }
